// Question 136: Use console.log() to debug and track the value of a variable inside a loop.

// using console.log() inside a loop to track variable values
for (let i = 0; i < 5; i++) {
    // Variable whose value we want to track
    let currentValue = i * 2;

    // Output the value of the variable
    console.log("Current value of currentValue:", currentValue);

}
  // This loop iterates five times, logging the value of 'i' during each iteration to help with debugging.
